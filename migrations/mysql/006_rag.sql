CREATE TABLE IF NOT EXISTS rag_retrieval_trace (
    id             BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    query          TEXT NOT NULL,
    route          VARCHAR(50) NOT NULL,
    vector_count   INT NOT NULL DEFAULT 0,
    keyword_count  INT NOT NULL DEFAULT 0,
    hybrid_count   INT NOT NULL DEFAULT 0,
    latency_ms     BIGINT NOT NULL DEFAULT 0,
    metadata       JSON,
    created_at     DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    updated_at     DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
    deleted_at     DATETIME(3) DEFAULT NULL,
    PRIMARY KEY (id),
    INDEX idx_route (route),
    INDEX idx_deleted_at (deleted_at),
    INDEX idx_route_created_at (route, created_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS rag_evidence (
    id              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    evidence_key    VARCHAR(120) NOT NULL,
    query           TEXT NOT NULL,
    chunk_id        BIGINT UNSIGNED NOT NULL,
    document_id     BIGINT UNSIGNED NOT NULL,
    source_id       BIGINT UNSIGNED NOT NULL,
    channel         VARCHAR(50) NOT NULL,
    title           VARCHAR(500) NOT NULL DEFAULT '',
    content         LONGTEXT,
    location        VARCHAR(500) NOT NULL DEFAULT '',
    score           DOUBLE NOT NULL DEFAULT 0,
    rerank_score    DOUBLE NOT NULL DEFAULT 0,
    token_estimate  INT NOT NULL DEFAULT 0,
    citation_uri    VARCHAR(1000) NOT NULL DEFAULT '',
    metadata        JSON,
    created_at      DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    updated_at      DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
    deleted_at      DATETIME(3) DEFAULT NULL,
    PRIMARY KEY (id),
    INDEX idx_evidence_key (evidence_key),
    INDEX idx_chunk_id (chunk_id),
    INDEX idx_document_id (document_id),
    INDEX idx_source_id (source_id),
    INDEX idx_channel (channel),
    INDEX idx_score (score),
    INDEX idx_rerank_score (rerank_score),
    INDEX idx_deleted_at (deleted_at),
    INDEX idx_channel_score_deleted_at (channel, score, deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

