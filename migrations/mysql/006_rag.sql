    INDEX idx_document_status_deleted_at (document_id, status, deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS rag_chunk (
    id              BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    chunk_key       VARCHAR(120) NOT NULL,
    document_id     BIGINT UNSIGNED NOT NULL,
    source_id       BIGINT UNSIGNED NOT NULL,
    ordinal         INT NOT NULL DEFAULT 0,
    title           VARCHAR(500) NOT NULL DEFAULT '',
    content         LONGTEXT,
    content_hash    VARCHAR(128) NOT NULL DEFAULT '',
    location        VARCHAR(500) NOT NULL DEFAULT '',
    token_estimate  INT NOT NULL DEFAULT 0,
    strategy        VARCHAR(50) NOT NULL DEFAULT '',
    metadata        JSON,
    created_at      DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    updated_at      DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
    deleted_at      DATETIME(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_rag_chunk_key (chunk_key),
    INDEX idx_document_id (document_id),
    INDEX idx_source_id (source_id),
    INDEX idx_ordinal (ordinal),
    INDEX idx_content_hash (content_hash),
    INDEX idx_strategy (strategy),
    INDEX idx_deleted_at (deleted_at),
    INDEX idx_document_ordinal_deleted_at (document_id, ordinal, deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS rag_embedding (
    id           BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    chunk_id     BIGINT UNSIGNED NOT NULL,
    model        VARCHAR(120) NOT NULL,
    dimension    INT NOT NULL DEFAULT 0,
    vector_ref   VARCHAR(200) NOT NULL DEFAULT '',
    vector_hash  VARCHAR(128) NOT NULL DEFAULT '',
    status       VARCHAR(20) NOT NULL DEFAULT 'pending',
    error        TEXT,
    metadata     JSON,
    created_at   DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    updated_at   DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
    deleted_at   DATETIME(3) DEFAULT NULL,
    PRIMARY KEY (id),
    INDEX idx_chunk_id (chunk_id),
    INDEX idx_model (model),
    INDEX idx_vector_ref (vector_ref),
    INDEX idx_vector_hash (vector_hash),
    INDEX idx_status (status),
    INDEX idx_deleted_at (deleted_at),
    INDEX idx_chunk_model_deleted_at (chunk_id, model, deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

