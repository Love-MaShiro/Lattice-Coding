-- RAG import, parsing, chunking, embedding, keyword indexing, retrieval, rerank, and evidence gate tables.

CREATE TABLE IF NOT EXISTS rag_source (
    id            BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    source_key    VARCHAR(120) NOT NULL,
    name          VARCHAR(200) NOT NULL,
    type          VARCHAR(50) NOT NULL,
    uri           VARCHAR(1000) NOT NULL DEFAULT '',
    description   TEXT,
    owner         VARCHAR(100) NOT NULL DEFAULT '',
    status        VARCHAR(20) NOT NULL DEFAULT 'active',
    config        JSON,
    metadata      JSON,
    created_at    DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    updated_at    DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
    deleted_at    DATETIME(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_rag_source_key (source_key),
    INDEX idx_name (name),
    INDEX idx_type (type),
    INDEX idx_owner (owner),
    INDEX idx_status (status),
    INDEX idx_deleted_at (deleted_at),
    INDEX idx_type_status_deleted_at (type, status, deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS rag_document (
    id             BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    document_key   VARCHAR(120) NOT NULL,
    source_id      BIGINT UNSIGNED NOT NULL,
    title          VARCHAR(500) NOT NULL,
    type           VARCHAR(50) NOT NULL DEFAULT 'unknown',
    uri            VARCHAR(1000) NOT NULL DEFAULT '',
    author         VARCHAR(200) NOT NULL DEFAULT '',
    version        VARCHAR(100) NOT NULL DEFAULT '',
    summary        TEXT,
    content_hash   VARCHAR(128) NOT NULL DEFAULT '',
    status         VARCHAR(20) NOT NULL DEFAULT 'pending',
    parser_name    VARCHAR(80) NOT NULL DEFAULT '',
    chunk_count    INT NOT NULL DEFAULT 0,
    metadata       JSON,
    created_at     DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    updated_at     DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3) ON UPDATE CURRENT_TIMESTAMP(3),
    deleted_at     DATETIME(3) DEFAULT NULL,
    PRIMARY KEY (id),
    UNIQUE KEY uk_rag_document_key (document_key),
    INDEX idx_source_id (source_id),
    INDEX idx_title (title),
    INDEX idx_type (type),
    INDEX idx_content_hash (content_hash),
    INDEX idx_status (status),
    INDEX idx_deleted_at (deleted_at),
    INDEX idx_source_status_deleted_at (source_id, status, deleted_at)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS rag_parsed_document (
    id            BIGINT UNSIGNED NOT NULL AUTO_INCREMENT,
    document_id   BIGINT UNSIGNED NOT NULL,
    parser_name   VARCHAR(80) NOT NULL DEFAULT '',
    content_text  LONGTEXT,
    structure     JSON,
